function adjustLights(lights) {
    let a = 0, b = 0;
    for (let i = 0; i < lights.length; i++) {
        if (i % 2 == 0) {
            if (lights[i] == "游릭") a++;
            else b++;
        } else {
            if (lights[i] == "游릭") b++;
            else a++;
        }
    }
    return Math.min(a, b)
}

adjustLights(["游릭", "游릭", "游릭", "游릭"]);
adjustLights(["游릭", "游댮", "游릭", "游댮"]);
adjustLights([""]);
adjustLights(["", ""]);
